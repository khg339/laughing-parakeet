package com.cameldev.mypage.domain;

public class majorDTO {
	private String major;
	private String intro;
	private String represent_professor;
	private String history;
	private String goal;
	private String facility;
	public String getMajor() {
		return major;
	}
	public void setMajor(String major) {
		this.major = major;
	}
	public String getIntro() {
		return intro;
	}
	public void setIntro(String intro) {
		this.intro = intro;
	}
	public String getRepresent_professor() {
		return represent_professor;
	}
	public void setRepresent_professor(String represent_professor) {
		this.represent_professor = represent_professor;
	}
	public String getHistory() {
		return history;
	}
	public void setHistory(String history) {
		this.history = history;
	}
	public String getGoal() {
		return goal;
	}
	public void setGoal(String goal) {
		this.goal = goal;
	}
	public String getFacility() {
		return facility;
	}
	public void setFacility(String facility) {
		this.facility = facility;
	}
	

}
